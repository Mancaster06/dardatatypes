void main() {
  //int to store numbers
  int age = 25;
  print("I am $age years old");

  double height = 5.9;
  //double to  store decimal numbers
  print("I am $height tall");

  String name = "Rajabu";
  // string to store text
  print("My name is $name");

  List<String> family = ["Saidi", "Omary", "Rahma"];
  //list to store ordered collection of items
  print("These are my family members: $family");

  Map<String, int> grades = {"Math": 90, "Physics": 92, "Chemistry": 67};
  //map to store key value pairs
  print("My grades are : $grades");
  //accessing elements from data structure
  print("Our firstborn is $family[1]");
}
